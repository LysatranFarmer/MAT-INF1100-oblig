from sympy import symbols, solve
from math import sqrt

def solveDifferenceEquation(x0, x1, n):
    xList = [x0, x1]
    for i in range(1, n):
        # denne for-lokken vil ikke fungere for start < 1.
        xList.append(4*xList[i]+xList[i-1])
        print(f"x{i+1} = {xList[i+1]}")

print(f"Oppgave 1a:")
solveDifferenceEquation(1, 1, 100)

print(f"Oppgave 1b:")
solveDifferenceEquation(1, (2-sqrt(5)), 100)


